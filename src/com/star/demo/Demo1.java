package com.star.demo;

public class Demo1 {
    private String name;
    private int age;

    public Demo1() {
    }

    public Demo1(String name) {
        this.name = name;
    }

    public Demo1(int age) {
        this.age = age;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
